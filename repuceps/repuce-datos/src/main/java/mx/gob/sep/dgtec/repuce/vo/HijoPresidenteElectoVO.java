package mx.gob.sep.dgtec.repuce.vo;

public class HijoPresidenteElectoVO {
	private Integer cCct;
	private String nivel;
	private String grado;
	private String acreditacion;
	private String hijo;
	
	public String getNivel() {
		return nivel;
	}
	public void setNivel(String nivel) {
		this.nivel = nivel;
	}
	public String getAcreditacion() {
		return acreditacion;
	}
	public void setAcreditacion(String acreditacion) {
		this.acreditacion = acreditacion;
	}
	public String getGrado() {
		return grado;
	}
	public void setGrado(String grado) {
		this.grado = grado;
	}
	public Integer getcCct() {
		return cCct;
	}
	public void setcCct(Integer cCct) {
		this.cCct = cCct;
	}
	public String getHijo() {
		return hijo;
	}
	public void setHijo(String hijo) {
		this.hijo = hijo;
	}

}
